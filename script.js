function initGame() {
  const cd = COUNTRIES_DATA[G.country];
  G.year = cd.startYear;
  G.money = cd.startMoney;
  G.stats = { ...cd.startStats };
  G.relations = {};
  Object.entries(cd.startRelations).forEach(([k, v]) => {
    G.relations[k] = { value: v, status: v >= 60 ? 'allied' : v >= 30 ? 'neutral' : 'hostile' };
  });
  G.charRelations = {};
  Object.keys(CHARACTERS).forEach(id => {
    G.charRelations[id] = { hearts: 0, value: 20, romanceStage: 0, dialogueKey: 'first_meeting' };
  });
  G.phase = 0;
  G.events = [];
  G.completedEvents = new Set();
  updateUI();
  renderRelationsTab();
  renderCharactersTab();
  renderTimeline();
  showScreen('game-screen');
  openTab('politics');
  queueNextEvent();
  notify(`Добро пожаловать! ${G.year} год. История ждёт вас.`);
}

