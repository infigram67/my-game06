function queueNextEvent() {
  const currentPhase = PHASES.find(p => p.year > G.year - 5 && p.year <= G.year + 10);
  if (!currentPhase) return;
  const availableEvents = currentPhase.events.filter(eid => !G.completedEvents.has(eid) && HISTORICAL_EVENTS[eid]);
  if (availableEvents.length > 0) {
    const eid = availableEvents[0];
    setTimeout(() => showEvent(eid), 1500);
  }
}

function showEvent(eid) {
  const ev = HISTORICAL_EVENTS[eid];
  if (!ev) return;
  const container = document.getElementById('eventDecision');
  if (!container) return;
  container.innerHTML = `
    <div class="event-choice-box fade-in">
      <div class="event-choice-title">⚡ ${ev.title}</div>
      <div class="event-choice-desc">${ev.desc}</div>
      <div class="event-choices">
        ${ev.choices.map((c, i) => `<button class="event-choice-btn" onclick="resolveEvent('${eid}', ${i})">${c.text}</button>`).join('')}
      </div>
    </div>`;
  addEvent(`📅 ${ev.year}: ${ev.title} — требует вашего решения!`);
  openTab('politics');
}

function resolveEvent(eid, choiceIdx) {
  const ev = HISTORICAL_EVENTS[eid];
  if (!ev) return;
  const choice = ev.choices[choiceIdx];
  if (!choice) return;
  G.completedEvents.add(eid);
  // Apply effects
  if (choice.effects) {
    Object.entries(choice.effects).forEach(([key, val]) => {
      if (key === 'money') G.money = Math.max(0, G.money + val);
      else if (key === 'relations_all') Object.keys(G.relations).forEach(k => {
        G.relations[k].value = Math.max(0, Math.min(100, G.relations[k].value + val));
      });
      else if (key in G.stats) G.stats[key] = Math.max(0, Math.min(100, G.stats[key] + val));
      else if (key === 'char_hitler') {
        if (G.charRelations.hitler) G.charRelations.hitler.value += val;
      }
    });
  }
  document.getElementById('eventDecision').innerHTML = `
    <div class="event-choice-box fade-in" style="border-color:var(--green-bright)">
      <div class="event-choice-title">✅ Решение принято</div>
      <div class="event-choice-desc">${choice.text_result}</div>
    </div>`;
  addEvent(`✅ ${ev.title}: ${choice.text_result}`);
  // Advance year
  G.year += 2 + Math.floor(Math.random() * 3);
  // Check phase advancement
  const nextPhase = PHASES.find(p => p.year > G.year && p.year <= G.year + 5);
  if (nextPhase) {
    G.year = nextPhase.year;
    notify(`Новая эпоха: ${nextPhase.name} (${nextPhase.year})`);
  }
  updateUI();
  renderCharactersTab();
  setTimeout(queueNextEvent, 3000);
}

function addEvent(text) {
  const log = document.getElementById('eventsLog');
  if (!log) return;
  const item = document.createElement('div');
  item.className = 'event-item fade-in';
  item.innerHTML = text;
  log.insertBefore(item, log.firstChild);
  while (log.children.length > 8) log.removeChild(log.lastChild);
}

