let novelQueue = [];
let novelChoices = [];
let novelCharId = null;

function startDialogue(charId) {
  const char = CHARACTERS[charId];
  if (!char) return;
  novelCharId = charId;
  const rel = G.charRelations[charId];
  const dialogueKey = rel.dialogueKey || 'first_meeting';
  const chain = char.dialogues[dialogueKey];
  if (!chain || chain.length === 0) {
    notify('Сейчас нет доступного диалога.');
    return;
  }
  G.currentChar = char;
  G.currentDialogChain = chain;
  G.currentDialogIndex = 0;
  openNovelOverlay(char);
  showNovelLine(chain[0]);
}

function openNovelOverlay(char) {
  const overlay = document.getElementById('novel-overlay');
  overlay.classList.remove('hidden');
  // Set portrait
  const portrait = document.getElementById('charPortrait');
  if (char.photo) {
    portrait.innerHTML = `<img src="${char.photo}" alt="${char.name}" onerror="this.innerHTML='👤'">`;
  } else {
    portrait.innerHTML = '👤';
  }
  // Set background
  document.getElementById('novelBg').style.backgroundImage = char.bg ? `url('${char.bg}')` : '';
  // Set name
  document.getElementById('novelCharName').textContent = char.name;
  // Update relation bar
  updateNovelRelBar(char.id);
}

function updateNovelRelBar(charId) {
  const rel = G.charRelations[charId] || { hearts: 0, value: 20 };
  const fill = document.getElementById('relBarFill');
  const hearts = document.getElementById('relHearts');
  const label = document.getElementById('relLabel');
  if (fill) fill.style.width = rel.value + '%';
  if (hearts) hearts.textContent = getHeartsDisplay(rel.hearts);
  const stage = rel.value >= 80 ? '💕 Близкие отношения' : rel.value >= 60 ? '😊 Дружеские' : rel.value >= 40 ? '😐 Нейтральные' : rel.value >= 20 ? '😕 Напряжённые' : '😡 Враждебные';
  if (label) label.textContent = `Отношения: ${stage}`;
  const char = CHARACTERS[charId];
  const moodEl = document.getElementById('charMood');
  if (moodEl && char) {
    const m = rel.value >= 70 ? '😊' : rel.value >= 50 ? '🙂' : rel.value >= 30 ? '😐' : '😠';
    moodEl.textContent = m;
  }
}

function showNovelLine(line) {
  if (!line) { showNovelChoices([]); return; }
  const text = typeof line.text === 'function' ? line.text() : line.text;
  document.getElementById('novelCharName').textContent = G.currentChar.name;
  document.getElementById('novelChoices').innerHTML = '';
  typeText('novelText', text, () => {
    if (line.choices && line.choices.length > 0) {
      showNovelChoices(line.choices);
    }
  });
  const moodEl = document.getElementById('charMood');
  if (moodEl && line.mood) moodEl.textContent = line.mood;
}

let typeInterval = null;
function typeText(elId, text, callback) {
  const el = document.getElementById(elId);
  if (!el) { if (callback) callback(); return; }
  if (typeInterval) clearInterval(typeInterval);
  el.textContent = '';
  let i = 0;
  const cursor = document.getElementById('typingCursor');
  if (cursor) cursor.style.display = 'inline';
  typeInterval = setInterval(() => {
    el.textContent += text[i];
    i++;
    if (i >= text.length) {
      clearInterval(typeInterval);
      typeInterval = null;
      if (cursor) cursor.style.display = 'none';
      if (callback) callback();
    }
  }, 28);
}

function showNovelChoices(choices) {
  const container = document.getElementById('novelChoices');
  if (!container) return;
  if (!choices || choices.length === 0) {
    container.innerHTML = `<button class="novel-choice-btn" onclick="continueNovel()">▶ Продолжить...</button>`;
    return;
  }
  container.innerHTML = choices.map((c, i) =>
    `<button class="novel-choice-btn ${c.type || ''}" onclick="chooseNovelOption(${i})">${c.text}</button>`
  ).join('');
}

function chooseNovelOption(idx) {
  const line = G.currentDialogChain[G.currentDialogIndex];
  if (!line || !line.choices) return;
  const choice = line.choices[idx];
  if (!choice) return;
  // Update relation
  const charId = novelCharId;
  if (!G.charRelations[charId]) G.charRelations[charId] = { hearts: 0, value: 20, romanceStage: 0, dialogueKey: 'first_meeting' };
  const rel = G.charRelations[charId];
  rel.value = Math.max(0, Math.min(100, rel.value + (choice.delta || 0)));
  rel.hearts = Math.min(5, Math.floor(rel.value / 20));
  updateNovelRelBar(charId);
  if (choice.delta > 0) addEvent(`💬 ${G.currentChar.name}: отношения улучшились (+${choice.delta})`);
  else if (choice.delta < 0) addEvent(`💬 ${G.currentChar.name}: отношения ухудшились (${choice.delta})`);
  // Follow dialogue branch
  if (choice.next) {
    const nextChain = G.currentChar.dialogues[choice.next];
    if (nextChain) {
      G.charRelations[charId].dialogueKey = choice.next;
      G.currentDialogChain = nextChain;
      G.currentDialogIndex = 0;
      showNovelLine(nextChain[0]);
      return;
    }
  }
  // Move to next in chain
  G.currentDialogIndex++;
  if (G.currentDialogIndex < G.currentDialogChain.length) {
    showNovelLine(G.currentDialogChain[G.currentDialogIndex]);
  } else {
    endDialogue(charId);
  }
}

function continueNovel() {
  G.currentDialogIndex++;
  if (G.currentDialogChain && G.currentDialogIndex < G.currentDialogChain.length) {
    showNovelLine(G.currentDialogChain[G.currentDialogIndex]);
  } else {
    endDialogue(novelCharId);
  }
}

function endDialogue(charId) {
  // Set next dialogue key for return visits
  const rel = G.charRelations[charId];
  if (rel) {
    // Cycle through available dialogues on repeat visits
    const char = CHARACTERS[charId];
    const keys = Object.keys(char.dialogues).filter(k => k !== 'first_meeting');
    if (rel.value >= 60 && keys.length > 0) {
      const randomKey = keys[Math.floor(Math.random() * keys.length)];
      rel.dialogueKey = randomKey;
    }
  }
  document.getElementById('novelChoices').innerHTML = `
    <button class="novel-choice-btn positive" onclick="closeNovel()">До свидания...</button>`;
  typeText('novelText', '*(Разговор завершён. Вы можете поговорить снова позже.)*', null);
  document.getElementById('novelCharName').textContent = '';
  renderCharactersTab();
}

function skipDialog() {
  if (typeInterval) {
    clearInterval(typeInterval);
    typeInterval = null;
    const line = G.currentDialogChain?.[G.currentDialogIndex];
    if (line) {
      const text = typeof line.text === 'function' ? line.text() : line.text;
      document.getElementById('novelText').textContent = text;
      if (line.choices) showNovelChoices(line.choices);
    }
  }
}

function closeNovel() {
  document.getElementById('novel-overlay').classList.add('hidden');
  if (typeInterval) { clearInterval(typeInterval); typeInterval = null; }
}

